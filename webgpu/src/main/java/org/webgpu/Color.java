package org.webgpu;

public class Color {
    public float r;
    public float g;
    public float b;
    public float a;

    public Color() {
        this.r = 0;
        this.g = 0;
        this.b = 0;
        this.a = 0;
    }
    
    public Color(float r, float g, float b, float a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }

    
}